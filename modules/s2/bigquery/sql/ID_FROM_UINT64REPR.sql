CREATE OR REPLACE FUNCTION `@@BQ_PREFIX@@s2.ID_FROM_UINT64REPR`
(x STRING)
RETURNS INT64
AS (
IF
(CAST(x AS BIGNUMERIC) > 9223372036854775807,
  CAST(CAST(x AS BIGNUMERIC)-CAST('18446744073709551616' AS BIGNUMERIC)AS INT64),
  CAST(x AS INT64)));

