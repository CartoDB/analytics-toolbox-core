CREATE OR REPLACE EXTERNAL FUNCTION @@SCHEMA@@.__QUADBIN_BOUNDARY
(BIGINT)
-- (quadbin)
RETURNS VARCHAR(MAX)
STABLE
LAMBDA '@@LAMBDA_ARN@@'
IAM_ROLE '@@IAM_ROLE_ARN@@';

CREATE OR REPLACE FUNCTION @@SCHEMA@@.QUADBIN_BOUNDARY
(BIGINT)
-- (quadbin)
RETURNS GEOMETRY
STABLE
AS $$
    SELECT ST_GEOMFROMTEXT(@@SCHEMA@@.__QUADBIN_BOUNDARY($1), 4326)
$$ LANGUAGE sql;
